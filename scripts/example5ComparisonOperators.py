# Equal: == Check if two values are equal to each other
print(12 == 12) # SAME AS print(12 is 12)
# Not Equal: != Check if two values aren't equal
print(9 != 10) # SAME AS print(12 is not 12)
# Greater than >
print(10 > 100)
# Less than 
print(1 < 1000)
# Greater than or equal to
print(12 >= 12)
# Less than or equal to
print(15 <= 12)